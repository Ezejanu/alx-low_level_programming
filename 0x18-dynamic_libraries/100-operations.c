
int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);

/**
 * add - a function to add a and b
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - a function to subtract a and b
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int sub(int a, int b)
{
	return (a - b);
}



/**
 * mul - a function to multiply a and b
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int mul(int a, int b)
{
	return (a * b);
}



/**
 * div - a function to divide a and b
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int div(int a, int b)
{
	return (a / b);
}



/**
 * mod - a function to find the modular of a and b
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int mod(int a, int b)
{
	return (a % b);
}
